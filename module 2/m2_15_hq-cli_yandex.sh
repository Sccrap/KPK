#!/bin/bash
###############################################################################
# m2_15_hq-cli_yandex.sh — Установка Яндекс Браузера на HQ-CLI (ALT Linux)
# Задание 9 (из документа): Установить Яндекс Браузер
###############################################################################
set -e

# =============================================================================
echo "=== [1/2] Обновление репозиториев ==="

apt-get update -y
echo "  Репозитории обновлены"

# =============================================================================
echo "=== [2/2] Установка Яндекс Браузера ==="

apt-get install -y yandex-browser 2>/dev/null || {
    echo "  Пакет yandex-browser не найден в репозиториях"
    echo "  Попробуем yandex-browser-stable..."
    apt-get install -y yandex-browser-stable 2>/dev/null || {
        echo "  ОШИБКА: Яндекс Браузер не найден"
        echo "  Проверьте доступность репозиториев: apt-cache search yandex"
        exit 1
    }
}

echo ""
echo "=== Яндекс Браузер установлен ==="
echo "Найдите его в: Меню → Все программы → Интернет → Yandex Browser"
echo "Для добавления на рабочий стол: ПКМ → Добавить на рабочий стол"
